# 86: Crie um script com um loop for, com elementos para uma pizza ,
# para cada elemento vai ser exibida a mensagem exemplo ‘ adicionando tomate’, ]
# por ultimo faça uma mensagem ‘pizza pronta’.

ingredientes = ["Tomate", "Queijo", "Presunto", "Bacon", "Azeitona", "Pimenta"]

for item in ingredientes:
    print("Adicionando", item, "")

print("Pizza Pronta!")