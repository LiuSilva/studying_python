# 60: Explique sobre indentação
'''
A indentação no Python é algo muito importante, pois isso delimita os blocos de codigos dentro do arquivo
assim, é importante se preocupar com a indentação ao escrever codigos em Python.

PEP 8 recomenda que use 4 espaços para delimitar os blocos em Python, e os níveis de indentação
e isso faz com que o programa fique melhor para ler.

O interpretador do Python pode ficar confuso quando o programador mistura tabs e espaços, pois o tamanho do tab
depende de como foi configurado o editor que o programador esta usando, assim ele pode ter mais ou menos que
4 espaços, e assim o interpretador fica confuso sobre as delimitações de blocos. Misturar espaços e tabs no 
arquivo pode causar problemas que são muito dificies de encontrar. Assim é melhor usar apenas espaços
ou apenas tabs para indentar o programa durante todo o arquivo.
'''