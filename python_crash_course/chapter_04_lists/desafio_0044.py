numeros = [10, 15, 2, 50, 5, 16, 18, 20, 33, 54, 10, 11]

print(numeros)

print("\nO menor numero da lista é:", min(numeros))

print("\nO maior numero da lista é:", max(numeros))

print("\nA soma dos numeros da lista é:", sum(numeros))

