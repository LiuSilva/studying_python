"""
and - e
or - ou
not - não
is - é
is not - não é
in - esta contido
not in - não esta contido
"""
print("2 < 4 and 2 == 4 => ", (2 < 4 and 2 == 4))
print("2 > 4 or 2 < 4 => ", (2 > 4 or 2 < 4))
print("not(2 > 4 or 2 < 4) => ", (not(2 > 4 or 2 < 4)))
print("2 is 2 => ", (2 is 2))
print("type(2) is int => ", (type(2) is int))
print("type(2.0) is int => ", (type(2.0) is int))
print("type(2.0) is float => ", (type(2.0) is float))