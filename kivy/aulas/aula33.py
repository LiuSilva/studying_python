s = "Para o Python, toda String é uma lista imutável."
print(s)

#para Python tudo é string, por ter de 1 a quanto memoria suportar
print("s[0] =", s[0])
print("s[-1] =", s[-1])

# fatiamentno
print("s[7:13] =", s[7:13])
print("s[5:20] =", s[5:20])
print("s[5:] =", s[5:])
print("s[::-1] =", s[::-1])
print("s[::2] =", s[::2])
print("s[::5] =", s[::5])
