# Ordenando Listas - Aula 60
lista = ['Cláudio', 'José', 'Maria', 'Beltrano', 'Fulano', 'João', 'Ciclano', ]
print(lista)

# Ordenando de tras pra frente
lista.reverse()
print("lista.reverse() =", lista)

# Ordenar de forma ascendente
lista.sort()
print("lista.sort() =", lista)

# Ordenar de forma descendente
lista.sort(reverse=True)
print("lista.sort(reverse=True) =", lista)