# lista de listas

lista = [['a', 'b', 'c'], [5, 8, 2, 4], []]
print("lista = ", lista)
print("lista[0] = ", lista[0])
print("lista[2] = ", lista[2])
print("lista[0][2] = ", lista[0][2])
print("lista[1][1] = ", lista[1][1])
print("len(lista) = ", len(lista))
print("len(lista[1]) =", len(lista[1]))
print("len(lista[2]) =", len(lista[2]))
print("len('eXcript') =", len('eXcript'))

# no Python usa -1 pra saber o ultimo item da lista :o
print("lista[len(lista)-1] =", lista[len(lista)-1])
print("lista[-1] =", lista[-1])
print("lista[0][-1] =", lista[0][-1])
print("lista[1][-1] =", lista[1][-1])